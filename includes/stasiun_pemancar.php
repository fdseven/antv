<p>
Pada tanggal 7 Mei 2012, Eny yang memiliki pengalaman kerja selama delapan belas tahun dibidang accounting, audit, tax planning, finance, collection, leasing, budgeting & controlling ini, bergabung sebagai GM Finance di ANTV. Menjelang pergantian tahun, pada 1 Desember 2013 Eny menjabat sebagai Chief Finance Officer(CFO).</p>

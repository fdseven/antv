            <h5 class="head_title">2008</h5>
            <article>
<p>
Meraih penghargaan Gold Award - Kategori Best News & Current Affairs PROMAX ASIA 2008, Topik versi Monjay tgl 18 November 2008 dan penghargaan Bronze Award kategori media cetak Citra Adi Pariwara 2008, The Simpson Hadir di Indonesia, tgl 14 November 2008.</p>
<p>
Meraih penghargaan yang diberikan oleh Pemerintah Daerah Bantul dalam rangka memperingati Hari Jadi Kabupaten Bantul ke-177, pada 20 Juli 2008, kepada media massa (ANTV Peduli) yang turut membantu pasca gempa 27 Mei 2006 di Jogyakarta dan Bantul dengan kategori penghargaan :</p>
<p>
Penghargaan Bhakti Grawitya bidang Pendidikan
Penghargaan Bhakti Sasana Gama bidang Pembangunan Sarana Agama
Penghargaan Bhakti Waratama bidang Pemberitaan
Penghargaan Bhakti Grha Husada bidang kesehatan</p>
<p>
Meraih Muctar Lubis Award, pada tanggal 18 Juli 2008, penghargaan sebagai Juara Kategori In- Dept TV Reporting dengan karya Mengeruk Laba dari Bangkai Sapi. Diselenggarakan oleh Lembaga Studi Pers dan Pembangunan (LSPP).</p>
<p>
Meraih penghargaan Life Award 2008, 26 Juni 2008. Penghargaan diberikan kepada ANTV sebagai Kategori Investigasi lewat Program Telisik Bisnis Narkoba di dalam Penjara. Diselengarakan oleh UNODC, YCAB dan Media Group.</p>
<p>
Meraih penghargaan Vidya Satya Nugraha 2007, 19 Juni 2008, penghargaan diberikan kepada ANTV sebagai Lembaga Penyiaran Televisi Yang paling banyak mengirimkan materi-materi untuk di sensor tahun 2007.</p>
<p>
Meraih Anugrah Hukum Indonesia bagi Media Massa, 1 April 2008, penghargaan diberikan sebagai Kategori Televisi Kepada Program Mata Rantai, Narapidana di Luar Penjara Episode Kamis, 20 Maret 2008”</p>
<p>
Meraih penghargaan KPI AWARD 2007, pada tanggal 18 Maret 2008, penghargaan diberikan kepada ANTV berdasarkan penilaian para juri untuk :</p>
<ol>
   <li>
   Program Telisik Liputan Bisnis 
   Narkoba di Dalam Penjara, 
   memenangkan kategori Program  
   Berita Investigasi.</li>
   <li>
   Program Topik Kita Waspada 
   Aliran Sesat, memenangkan 
   kategori Program Talk Show.</li>
   <li>
   Program I-Sinema Siapa Sayang 
   Lila, memenangkan kategori 
   sinetron lepas atau FTV.</li>
</ol>
<p>
Meraih Juara Pertama Lomba Karya Jurnalis Olahraga Kategori Televisi diselenggarakan oleh Sirkuit Sentul dalam karya Grand Racing Sirkuit Sentul di Lensa Olahraga, 16-17 Februari 2008.”</p>
<p>
Meraih Juara Pertama Lomba Karya Jurnalistik Televisi dalam rangka peringatan hari anti korupsi sedunia diselenggarakan oleh KOMTAK (Komunitas Tionghoa Anti Korupsi) bekerjasama dengan Radio Ramako dan Pertamina dalam karya Liputan Investigasi Praktek Korupsi Lembaga Permasyarakatan Paledang.</p>

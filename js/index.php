          <td align="center" valign="middle"><p align="justify"><span class="style4">KELOMPOK USAHA BAKRIE BERSINERGI BAKTI SOSIAL BANTU KORBAN BANJIR</span></p>
            <p align="justify">Jakarta, 12 Maret 2014 – Di musim hujan ini, Kelompok Usaha Bakrie (KUB) kembali bersinergi dalam  kegiatan sosial yang dipimpin oleh Anindya N. Bakrie dan Ardiansyah A. Bakrie. Kelompok Usaha Bakrie yang bersinergi antara lain Bakrie Untuk Negeri,Bakrie Amanah,Induk Koperasi Bakrie,Energi Mega Persada, Bakrie Global, KPC, VIVA Group, Inter Media Capital, Bakrie & Brothers, Bakrie Sumatera Plantation, Bakrie Swasasti Utama, Bakrie Metal Industries, Braja Mukti Cakra, Bumi Resources, Lapindo Brantas, Bakrie Telecom, Universitas Bakrie, VIVAcoid, tvOne, ANTV. Kali ini KUB mengadakan kegiatan sosial berupa bantuan untuk SD & SMP Perguruan Rakyat 2 yang terletak di jalan Kampung Melayu Kecil I /38 Bukit Duri, Tebet, Jakarta Selatan. Selain memberikan bantuan berupa materi, KUB juga akan terjun langsung membersihkan sekolah tersebut. Kegiatan dilaksanakan Rabu 12 Maret 2014.<br />
            </p>
            <p align="justify">SD & SMP Perguruan Rakyat 2 yang berdiri sejak tahun 1972 ini, sempat terhenti proses belajar mengajar selama satu bulan lebih akibat banjir yang sekarang ini sudah menjadi musibah tahunan di Indonesia khususnya di Ibukota Jakarta. Bantuan yang materi yang diberikan berupa perlengkapan belajar mengajar dan perlengkapan kebersihan seperti papan tulis, buku tulis, seragam, tas sekolah, genset, lemari buku, dll.<br />
            
<br />KUB memilih sekolah SD & SMP Perguruan Rakyat 2 untuk dibantu karena sampai saat ini kondisi sekolah sangat parah dan belum mendapatkan bantuan dari pihak manapun. Selain itu, sekolah ini merupakan milik Yayasan Perguruan Rakyat yang para pendirinya adalah mantan pencetus sumpah pemuda, seperti Mr. Sunaryo, Mr. Sumanang dan Muhammad Rasyid. Orang tua siswa SD & SMP Perguruan Rakyat 2 kebanyakan bekerja sebagai buruh serta pedagang asongan yang mayoritas tinggal di daerah Kampung Melayu dan Kampung Pulo. Di SD & SMP Perguruan Rakyat 2 ini, terdapat 6 kelas setingkat Sekolah Dasar, dan 6 kelas setingkat SMP pada siang harinya. Murid-murid SD berjumlah 142 siswa, dan murid SMP sebanyak 136 siswa. 

“Kegiatan Baksos ini merupakan kepedulian terhadap musibah yang dialami oleh sekitar lingkungan kita. Yaitu Musibah Banjir di kawasan yg paling parah terkena dampak banjir yaitu di kawasan Kampung Melayu” Ujar Anindya N. Bakrie selaku Pimpinan Bakti Sosial KUB. “Mudah-mudahan apa yang dapat kami berikan dan kami bantu bermanfaat bagi SD & SMP Perguruan Rakyat 2” lanjut Anin.
  

                <h5 class="head_title"><span>ANTV TAYANGKAN BEST</span> <br class="hidden-xs">MATCH PIALA DUNIA</h5>
                <article>
                 <p>JAKARTA --- Komitmen ANTV sebagai stasiun televisi yang menayangkan semua (64 match) pertandingan Piala Dunia 2014 Brasil terus dikukuhkan dengan tayangan-tayangan pemanasan menuju Brasil 2014. Salah satu program terbaru yang akan dirilis adalah Fantastic World Cup Best Match (FWCBM). Acara tayang setiap Jumat, mulai 10 Januari 2014 mulai pukul 23.55 WIB.</p>
<p>
Sesuai namanya, FWCBM akan menyajikan pertandingan-pertandingan terbaik sepanjang sejarah piala dunia. Tentu saja pertandingan Final Piala Dunia menjadi menu utama. Diawali dengan episode final piala dunia 1966, dimana era televsi mulai ada, hingga final PD 2010.</p>
<p>
"Selain partai final dalam Best Match juga menghadirkan partai kejutan dan spetakuler yang sampai kini masih dikenang. Seperti laga Kamerun mengalahkan Argentina"di Piala Dunia 1990 di Italia, dan Korsel menekuk Itali di Piala Dunia 2002." kata Reva Dedddy Utama, Direktur Olahraga ANTV.</p>
<p>
Program ini dipandu seorang presenter cantik yang akan mengajak pemirsa untuk menyaksikan betapa akbar menariknya pertandingan yang akan disuguhkan, dengan kualitas gambar dari hasil rekaman terbaru</p>
